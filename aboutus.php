<h1>What Is NASA?<h1>
<h3>NASA stands for National Aeronautics and Space Administration. NASA is a U.S. government agency that is responsible for science and technology related to air and space. The Space Age started in 1957 with the launch of the Soviet satellite Sputnik.
NASA opened for business on Oct. 1, 1958. The agency was created to oversee U.S. space exploration and aeronautics research.
The administrator is in charge of NASA. The NASA administrator is nominated by the president and confirmed by a vote in the Senate.
</h3>

<h1>NASA Leadership</h1>
<h3>Administrator Jim Bridenstine
Portrait of Jim Bridenstine
NASA Administrator Jim Bridenstine, Official Portrait, Wednesday, July 24, 2019, NASA Headquarters in Washington.
Credits: NASA/Bill Ingalls
James Frederick “Jim” Bridenstine was nominated by President Donald Trump, confirmed by the U.S. Senate, and sworn in as NASA’s 13th administrator on April 23, 2018. Bridenstine was elected in 2012 to represent Oklahoma’s First Congressional District. In Congress he served on the Armed Services Committee and the Science, Space and Technology Committee. Bridenstine’s career in federal service began in the U.S. Navy, flying the E-2C Hawkeye off the USS Abraham Lincoln aircraft carrier. He flew combat missions in Iraq and Afghanistan and accrued most of his 1,900 flight hours and 333 carrier-arrested landings.
<h3>
<h1>Deputy Administrator James W. Morhard</h1>
<h3>James Morhard was nominated by President Trump and confirmed to be NASA’s 14th Deputy Administrator. He was sworn in on October 17th, 2018.

Official Portrait of Jim Morhard
Portrait, Jim Morhard, Deputy Administrator, National Aeronautics and Space Administration (NASA), Wednesday, Oct. 24, 2018 at NASA Headquarters in Washington.
Credits: NASA/Bill Ingalls
Jim helps provide overall leadership, planning, and policy direction. He performs duties and exercises powers delegated by the Administrator, assists him in making final agency decisions, and acts in his absence to govern NASA operations. Jim also is responsible for articulating and representing the agency's vision.
</h3>
